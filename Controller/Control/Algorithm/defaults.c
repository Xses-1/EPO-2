int **getDefaultBoard()
{
	int **board = (int **) malloc(sizeof(int *) * N_ROWS);
	for (int i = 0; i < N_ROWS; ++i)
	{
		board[i] = (int *) malloc(sizeof(int) * N_COLS);
		for (int j = 0; j < N_COLS; ++j)
		{
			board[i][j] = DEFAULT_BOARD[i][j];
		}
	}
	return board;
}
